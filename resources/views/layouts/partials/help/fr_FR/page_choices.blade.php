<p>
    Les choix sont des liens en fin de page, vers lesquels le joueur sera redirigé lorsqu'il cliquera dessus.
</p>
<p>
    En haut de la page se trouve le bouton <button class="btn btn-primary text-white"><span class="icon-choice mr-2 text-white"></span>Ajoute un nouveau choix</button>. En cliquant dessus tu ouvres une popup assez minimaliste.
</p>

<h4>Vers une nouvelle page</h4>

<p>
    Par défaut, tu vas créer un choix vers une nouvelle page. Il te suffira alors de renseigner le champ Texte du choix, et
    de laisser la coche sur l'option "Afficher la page nouvellement créée".
</p>
<p>
    Le Texte du choix est le texte qui apparaîtra sur le lien sur lequel le joueur cliquera. Ce n'est pas le titre de la page
    que tu t'apprêtes à créer ! Celui-ci te sera demandé à l'étape suivante, lors de la création de la nouvelle page.
</p>
<p>
    Prenons un exemple : le Texte du choix "Aller vers le manoir" sera un lien vers la page dont le titre est "Le manoir".
</p>

<h4>Vers une page existante</h4>

<p>
    Souvent tu auras besoin de renvoyer le joueur vers une page déjà existante. Il te suffira alors de la choisir dans la
    liste déroulante, où tu trouveras tous les titres des pages existantes. Sauf évidemment celles qui sont déjà liées à
    la page en cours.
</p>

<h4>Arborescence des pages</h4>

<p>
    Sur le côté droit de la fenêtre d'édition est représentée l'arborescence de ton histoire. Tu y vois les différentes pages,
    ainsi que les choix qui vont et viennent de chacune d'entre elles. Tu peux accéder à n'importe quelle page en cliquant
    sur son titre, et modifier n'importe quel choix en cliquant sur l'icône <span class="icon-fountain-pen"></span> se trouvant à côté du texte du choix.
</p>
<p>
    Tu peux également supprimer un choix en cliquant sur l'icône <span class="icon-trash text-red"></span>. Note que cela ne supprime
    que le lien entre deux pages, pas les pages elles-mêmes.
</p>

<h2><span class="icon-papers mr-2"></span>Liste des pages</h2>

<p>
    A nouveau en bas de la page tu trouveras le bouton <button class="btn btn-primary text-white"><span class="icon-papers mr-2 text-white"></span>Voir toutes les pages</button>. Il te permet d'avoir une vue d'ensemble des
    textes des pages, ainsi que des choix de chacune. Tu peux y supprimer une page, à condition qu'elle n'ait ni parent (aucun
    choix ne mène vers elle) ni enfant (aucun choix ne part d'elle).
</p>
