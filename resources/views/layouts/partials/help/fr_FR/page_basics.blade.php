<h3>Le titre</h3>

<p>
    Le titre est important lors de la création de la page. Il permet de retrouver cette dernière dans l'arborescence de l'histoire,
    de manière unique. Il peut être un simple numéro, comme pour un chapitre, ou une phrase complète. Il sera affiché en haut de la page
    lorsque le joueur arrivera dessus.
</p>

<h3>Le contenu</h3>

<p>
    C'est là que se trouvent les informations à afficher au joueur. Que ce soit une description de son environnement, des actions en cours
    ou une liste d'objets sur lesquels interagir, tout se passe ici.
</p>
<p>
    L'éditeur permet des mises en forme complexe, allant du gras à l'italique, à l'ajout de tableaux, d'images ou de liens.
</p>

<h3>Fin de l'histoire</h3>

<p>
    Cette option permet de dire si la page en cours d'édition est la dernière de l'histoire, ou en tout cas une des dernières. Si cette case
    est cochée, aucun choix ne peut être ajouté à la page car l'aventure du joueur s'arrêtera à cet endroit.
</p>

<h3>Point de sauvegarde</h3>

<p>
    Si cette case est cochée, le joueur pourra revenir sur cette page quand il le désire. Autrement il n'est pas possible de revenir en arrière.
</p>
