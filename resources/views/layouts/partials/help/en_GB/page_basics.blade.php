<h3>Title</h3>

<p>
    Title is important when you create a new page. You will then be able to target it in the graphical tree,
    or you can later link to it easily. It can be a sentence or a single number. It is displayed to the player.
</p>

<h3>Content</h3>

<p>
    This is the text of your story. This can be a description, a dialog, images... Riddles and items the player
    will be able to take will also appear there.
</p>
<p>
    The editor allows you to easily format your text with italic, bold, tables, imgages, links...
</p>

<h3>End of the story</h3>

<p>
    This option will tell the player that the story is over. You won't be able to create choices from this page
    anymore. You can choose the kind of ending it is: if it is a happy one, a bad one, or if the player died.
</p>

<h3>Save point</h3>

<p>
    If you check this box the player will be able to go back to this particuliar page. Otherwise it is not possible.
</p>
